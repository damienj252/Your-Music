import sqlite3
from flask import g

DATABASE = 'http://127.0.0.1:5984/_utils/#/database/music/_all_docs'

def get_db():
    db = getattr(g, '_database', None)
    if db is None:
        db = g._database = sqlite3.connect(DATABASE)
    return db

@app.teardown_appcontext
def close_connection(exception):
    db = getattr(g, '_database', None)
    if db is not None:
        db.close()
